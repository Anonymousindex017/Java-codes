public class upperandlowercase {
    public static void main (String[]args){
        String S1= "Mera Naam Ravi Hai";  //String declaration
        String S2 ="Mai Ek Dancer Hu";  //String declaration
        System.out.println("Convert Upper case :"+S1.toUpperCase());  //Converts the string to upper case
        System.out.println("Convert Lower case :"+S2.toLowerCase());  //Converts the string to lower case
    }
    
}
